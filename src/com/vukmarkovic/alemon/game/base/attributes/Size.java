package com.vukmarkovic.alemon.game.base.attributes;

public class Size {
	private int width = 0;
	private int height = 0;
	
	public int getWidth() {
		return this.width;
	}
	public int getHeight() {
		return this.height;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
}
